我們的教育常常是一連串未經思考後的謬論。

眼見許多似是而非的儀式如洪流般無可抵擋，舉個例子，數年前開始，教育部為了弘揚孝道，大力推廣「弟子規」，於是幼稚園與小學生開始瘋狂的被迫在毫不理解與毫無意識的狀態下，如同被催眠般的背誦著「弟子規」。


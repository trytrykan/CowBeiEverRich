喔買尬，這太不科學！根本是代代相傳的盲從！基本上「弟子規」是一部毫無文學性可言的著作，與數來寶的差別不大，不就是花了點時間押韻？大家有思考過嗎？此時此刻正在臉書上的你我，哪個人小時候沒背過三字經？有誰的國學能力因此變好？或者因此成為寫作能人？

